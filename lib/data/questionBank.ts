import type { LessonArcQuestion } from "@/lib/lessonArc/types";

function defineQuestionBank(
  concept: string,
  questions: Array<Omit<LessonArcQuestion, "concept">>,
): LessonArcQuestion[] {
  return questions.map((question) => ({
    ...question,
    concept,
  }));
}

