export type MiniCoursePreviewMode = "console" | "canvas" | "dom" | "json";

export type ApiMiniCourseStage =
  | {
      id: string;
      kind: "tiles";
      prompt: string;
      note: string;
      codeLines: string[];
      tiles: string[];
      answer: string[];
      previewDefaults: string[];
    }
  | {
      id: string;
      kind: "typed";
      prompt: string;
      note: string;
      codeLines: string[];
      expected: string;
      placeholder: string;
      previewDefaults: string[];
      challengeType: "line" | "block";
    };

export type ApiMiniCourseLesson = {
  unitId: number;
  lessonId: number;
  title: string;
  subtitle: string;
  prompt: string;
  note: string;
  codeLines: string[];
  tiles: string[];
  answer: string[];
  previewDefaults: string[];
  previewLines?: string[];
  stages?: ApiMiniCourseStage[];
  previewMode: MiniCoursePreviewMode;
  previewKind: string;
  isChallenge?: boolean;
  challengeChecklist?: string[];
  expectedOutput?: string;
};

export type ApiMiniCourseUnit = {
  id: number;
  title: string;
  description: string;
  lessons: ApiMiniCourseLesson[];
};

export type ApiMiniCourse = {
  id: string;
  language: LearningLanguage;
  title: string;
  subtitle: string;
  description: string;
  units: ApiMiniCourseUnit[];
};

function normalizeApiCourseId(courseId?: string | null) {
  if (courseId === "pygame-zero") return "pygame";
  return courseId ?? null;
}

