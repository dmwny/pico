export function getApiMiniCourse(language?: string | null, courseId?: string | null) {
  const normalizedCourseId = normalizeApiCourseId(courseId);
  return EXPANDED_API_MINI_COURSES.find((course) => course.language === language && course.id === normalizedCourseId) ?? null;
}

export function getApiMiniCourseLessons(language?: string | null, courseId?: string | null) {
  return getApiMiniCourse(language, courseId)?.units.flatMap((unit) => unit.lessons) ?? [];
}

export function getApiMiniCourseLesson(
  language?: string | null,
  courseId?: string | null,
  unitId?: string | number,
  lessonId?: string | number
) {
  const course = getApiMiniCourse(language, courseId);
  const unit = course?.units.find((item) => item.id === Number(unitId));
  return unit?.lessons.find((lesson) => lesson.lessonId === Number(lessonId)) ?? null;
}

export function getApiMiniCourseKey(language: LearningLanguage, courseId: string, unitId: number, lessonId: number) {
  return `mini:${language}:${normalizeApiCourseId(courseId)}:${unitId}-${lessonId}`;
}

export function isApiMiniCourseKey(value: string, language: LearningLanguage, courseId: string) {
  const normalizedCourseId = normalizeApiCourseId(courseId);
  return value.startsWith(`mini:${language}:${normalizedCourseId}:`) || value.startsWith(`mini:${language}:pygame-zero:`) && normalizedCourseId === "pygame";
}

export function getCompletedApiMiniCourseKeys(
  completedLessons: string[] = [],
  language: LearningLanguage,
  courseId: string
) {
  return completedLessons.filter((value) => isApiMiniCourseKey(value, language, courseId));
}

export function isApiMiniCourseLessonUnlocked(
  course: ApiMiniCourse,
  completedLessons: string[],
  language: LearningLanguage,
  unitId: number,
  lessonId: number
) {
  if (unitId === 1 && lessonId === 1) return true;

  if (lessonId > 1) {
    return completedLessons.includes(getApiMiniCourseKey(language, course.id, unitId, lessonId - 1));
  }

  const previousUnit = course.units.find((unit) => unit.id === unitId - 1);
  if (!previousUnit) return false;

  return previousUnit.lessons.every((lesson) =>
    completedLessons.includes(getApiMiniCourseKey(language, course.id, lesson.unitId, lesson.lessonId))
  );
}

export function fillApiMiniCourseCode(codeLines: string[], values: string[], fallbacks: string[] = []) {
  let blankIndex = 0;

  return codeLines.map((line) =>
    line.replace(/_{3}/g, () => {
      const value = values[blankIndex] || fallbacks[blankIndex] || "0";
      blankIndex += 1;
      return value;
    })
  );
}
