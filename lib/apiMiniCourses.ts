function countCodeBlanks(codeLines: string[]) {
  return codeLines.reduce((total, line) => total + (line.match(/___/g)?.length ?? 0), 0);
}

const MINI_COURSE_COPY_REPLACEMENTS: Array<[RegExp, string]> = [
  [/\bfrom zero\b/g, "from scratch"],
  [/the game loop can keep running/g, "the game loop stays running"],
  [/the display surface is saved in screen/g, "the display surface is stored in the screen variable"],
  [/the collision result is stored in hit/g, "the collision result is stored in the hit variable"],
  [
    /Store the returned surface in screen so later code can draw onto it\./g,
    "Store the returned surface in the screen variable so later code can draw onto it.",
  ],
  [
    /Use x for left-right position and WIDTH for the edge of the screen\./g,
    "Use player_rect.right for the player's edge and WIDTH for the screen edge.",
  ],
];

function polishMiniCourseText(text: string) {
  return MINI_COURSE_COPY_REPLACEMENTS.reduce(
    (value, [pattern, replacement]) => value.replace(pattern, replacement),
    text.replace(/\s+/g, " ").trim()
  );
}

function polishMiniCoursePrompt(prompt: string, codeLines: string[]) {
  const polishedPrompt = polishMiniCourseText(prompt);
  const blankCount = countCodeBlanks(codeLines);

  if (blankCount === 1) {
    return polishedPrompt.replace(/^Fill the blanks so\b/, "Fill the blank so");
  }

  if (blankCount > 1) {
    return polishedPrompt.replace(/^Fill the blank so\b/, "Fill the blanks so");
  }

  return polishedPrompt;
}

function polishMiniCourseStage(stage: ApiMiniCourseStage): ApiMiniCourseStage {
  return {
    ...stage,
    prompt: polishMiniCoursePrompt(stage.prompt, stage.codeLines),
    note: polishMiniCourseText(stage.note),
  };
}

function polishMiniCourseLesson(lesson: ApiMiniCourseLesson): ApiMiniCourseLesson {
  return {
    ...lesson,
    title: polishMiniCourseText(lesson.title),
    subtitle: polishMiniCourseText(lesson.subtitle),
    prompt: polishMiniCoursePrompt(lesson.prompt, lesson.codeLines),
    note: polishMiniCourseText(lesson.note),
    stages: lesson.stages?.map(polishMiniCourseStage),
    challengeChecklist: lesson.challengeChecklist?.map(polishMiniCourseText),
    expectedOutput: lesson.expectedOutput ? polishMiniCourseText(lesson.expectedOutput) : undefined,
  };
}

function polishMiniCourse(course: ApiMiniCourse): ApiMiniCourse {
  return {
    ...course,
    title: polishMiniCourseText(course.title),
    subtitle: polishMiniCourseText(course.subtitle),
    description: polishMiniCourseText(course.description),
    units: course.units.map((unit) => ({
      ...unit,
      title: polishMiniCourseText(unit.title),
      description: polishMiniCourseText(unit.description),
      lessons: unit.lessons.map(polishMiniCourseLesson),
    })),
  };
}

